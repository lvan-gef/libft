#include "../../include/libft.h"

/**
* @brief Delete a node from the linked list
*
* @param lst Node to be deleted   (t_list*)
* @param del Function to delete the node (void (*)void*)
*
* @return void
*/
void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
